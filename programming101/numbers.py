# increment operator
some_number = 1
some_number = some_number + 1
some_number += 1
# what we are doing here is that we wanted to increment the some_number variable so a shorthand way of doing that is using += so that when we increment some_number it will automatically be stored in the some_number variable.

# decrement operator
a = 10
a -= 5
print(a)
